def checkVowel(n):
    match n:
        case 'a': return "Vowel Alphabet"
        case 'e': return "Vowel Alphabet"
        case 'i': return "Vowel Alphabet"
        case 'o': return "Vowel Alphabet"
        case 'u': return "Vowel Alphabet"
        case _: return "Simple Alphabet"
print (checkVowel('a'))
print (checkVowel('m'))
print (checkVowel('o'))
        